CREATE TABLE player (
    id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    uuid UUID NOT NULL,
    name VARCHAR(255) NOT NULL,
    profile_image BYTEA DEFAULT NULL,
    wins INT NOT NULL,
    last_heartbeat TIMESTAMP(0) WITHOUT TIME ZONE NOT NULL,
    PRIMARY KEY(id)
);

CREATE TABLE card (
    id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    value_left VARCHAR(255) NOT NULL,
    value_right VARCHAR(255) NOT NULL,
    PRIMARY KEY(id)
);

CREATE TABLE room (
    id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    game_active_player_id INT DEFAULT NULL,
    game_active_card_id INT DEFAULT NULL,
    uuid UUID NOT NULL,
    password VARCHAR(255) DEFAULT NULL,
    created_at TIMESTAMP(0) WITHOUT TIME ZONE NOT NULL,
    game_state VARCHAR(255) DEFAULT NULL,
    game_points_team_a INT DEFAULT NULL,
    game_points_team_b INT DEFAULT NULL,
    game_round_index INT DEFAULT NULL,
    game_target_degree DOUBLE PRECISION DEFAULT NULL,
    game_cluegiver_guess_text VARCHAR(255) DEFAULT NULL,
    PRIMARY KEY(id),
    CONSTRAINT FK_729F519BB16171F7 FOREIGN KEY (game_active_player_id) REFERENCES player (id) NOT DEFERRABLE INITIALLY IMMEDIATE,
    CONSTRAINT FK_729F519B134A7A72 FOREIGN KEY (game_active_card_id) REFERENCES card (id) NOT DEFERRABLE INITIALLY IMMEDIATE
);
CREATE INDEX IDX_729F519BB16171F7 ON room (game_active_player_id);
CREATE INDEX IDX_729F519B134A7A72 ON room (game_active_card_id);

CREATE TABLE guess (
    id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    player_id INT NOT NULL,
    room_id INT NOT NULL,
    degree DOUBLE PRECISION NOT NULL,
    is_preview BOOLEAN NOT NULL,
    PRIMARY KEY(id),
    CONSTRAINT FK_32D30F9699E6F5DF FOREIGN KEY (player_id) REFERENCES player (id) NOT DEFERRABLE INITIALLY IMMEDIATE,
    CONSTRAINT FK_32D30F9654177093 FOREIGN KEY (room_id) REFERENCES room (id) NOT DEFERRABLE INITIALLY IMMEDIATE
);
CREATE INDEX IDX_32D30F9699E6F5DF ON guess (player_id);
CREATE INDEX IDX_32D30F9654177093 ON guess (room_id);

CREATE TABLE room_players_team_a (
    room_id INT NOT NULL,
